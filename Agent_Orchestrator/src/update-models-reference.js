#!/usr/bin/env node
/**
 Regenerates Agent_Orchestrator/models-reference.md with the current known models
 and valid effort levels for all supported providers.

 Usage: node Agent_Orchestrator/update-models-reference.js
**/

'use strict';

const fs = require('fs');
const path = require('path');

// Output path: regenerated markdown table lives at harness root.
const HARNESS = path.join(__dirname, '..');
const OUT = path.join(HARNESS, 'models-reference.md');

// Load model-catalog for live Copilot cache reads (synchronous helpers only).
// Why: isModelAvailable reads the on-disk cache without network I/O, making
//      this script safe to call synchronously at any time.
const { getKnownModels, selectTiers } = require('./lib/model-catalog');

// Canonical Claude model + effort tables — the single source of truth this script renders.
const MODELS = [
  { id: 'claude-opus-4-8',          family: 'Opus',   alias: 'opus',   thinking: true  },
  { id: 'claude-sonnet-4-6',        family: 'Sonnet', alias: 'sonnet', thinking: true  },
  { id: 'claude-haiku-4-5-20251001',family: 'Haiku',  alias: 'haiku',  thinking: false },
];

const EFFORT_LEVELS = [
  { level: '""  / "none"', tokens: '—',                    families: 'All models'    },
  { level: '"auto"',       tokens: 'heuristic (see below)', families: 'Opus, Sonnet'  },
  { level: '"low"',        tokens: '1,024',                families: 'Opus, Sonnet'  },
  { level: '"medium"',     tokens: '5,000',                families: 'Opus, Sonnet'  },
  { level: '"high"',       tokens: '12,000',               families: 'Opus, Sonnet'  },
  { level: '"max"',        tokens: '32,000',               families: 'Opus, Sonnet'  },
];

// GPT-5 floor static fallback used when the Copilot model cache is absent or stale.
// Why: user requirement — never substitute below gpt-5 for github-copilot provider.
const COPILOT_STATIC_FALLBACK = ['gpt-5-mini', 'gpt-5'];

// Render helpers — produce one markdown table per dataset.
function modelTable() {
  const header = '| Model ID | Family | Alias (in config) | Extended Thinking |\n| -------- | ------ | ----------------- | ----------------- |';
  const rows = MODELS.map(m =>
    `| \`${m.id}\` | ${m.family} | \`${m.alias}\` | ${m.thinking ? 'Yes' : 'No'} |`
  );
  return [header, ...rows].join('\n');
}

function effortTable() {
  const header = '| Effort | Budget Tokens | Supported By |\n| ------ | ------------- | ------------ |';
  const rows = EFFORT_LEVELS.map(e =>
    `| \`${e.level}\` | ${e.tokens} | ${e.families} |`
  );
  return [header, ...rows].join('\n');
}

// Renders the GitHub Copilot models table from cache or static fallback.
// Why: models-reference.md must be the unified reference for both Claude and Copilot
//      validation. Sorting IDs before render ensures idempotent output across re-runs.
function copilotTable() {
  // getKnownModels reads the cache directly — no sentinel hack needed.
  const knownList = getKnownModels('github-copilot');

  let models;
  if (knownList.length > 0) {
    // Filter to GPT-family IDs and sort lexicographically for determinism.
    models = knownList
      .filter(id => /^gpt-/i.test(id))
      .sort();
    // Ensure at least the gpt-5 floor is present even when cache predates gpt-5.
    if (models.length === 0) models = [...COPILOT_STATIC_FALLBACK];
  } else {
    // Cache absent or stale — use GPT-5-floor static fallback.
    models = [...COPILOT_STATIC_FALLBACK];
  }

  // Compute tier assignments from the resolved model list.
  const tiers = selectTiers(models, 'github-copilot');

  const header = '| Model ID | Tier(s) |\n| -------- | ------- |';
  const rows = models.map(id => {
    const labels = [];
    if (tiers.heavy  === id) labels.push('heavy');
    if (tiers.medium === id) labels.push('medium');
    if (tiers.light  === id) labels.push('light');
    return `| \`${id}\` | ${labels.join(', ') || '—'} |`;
  });
  return [header, ...rows].join('\n');
}

// Final document body: auto-gen warning + provider sections + usage example.
// Title renamed from "Claude Models Reference" to "Models Reference" to reflect
// that the document now covers all supported providers (Claude + GitHub Copilot).
const content = `# Models Reference

> Auto-generated by \`update-models-reference.js\`. Run \`node Agent_Orchestrator/update-models-reference.js\` to refresh.

## Valid Models

${modelTable()}

## Valid Effort Levels

${effortTable()}

## GitHub Copilot Models

${copilotTable()}

## Usage in \`topic-config.json\` / \`global-config.json\`

\`\`\`json
"models": {
  "planning": "opus",
  "coding": "sonnet",
  "assessment": "",
  "effort": "medium"
}
\`\`\`

- \`effort\` applies to all roles unless a per-role override is set (e.g. \`"coding_effort": "high"\`).
- Empty string or \`"none"\` disables extended thinking (default).
- Applying an effort level to Haiku (which does not support extended thinking) will cause a CLI error.
`;

fs.writeFileSync(OUT, content, 'utf8');
console.log(`[update-models-reference.js] Written: ${path.relative(path.join(HARNESS, '..'), OUT)}`);
